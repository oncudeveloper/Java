package Loop;

public class Main {
    public static void main(String[] args) {
        for(int i=1;i<4;i++){
            System.out.println("i :"+i);
        }
        int j = 1;
        while(j<4){
            System.out.println("j :"+j);
            j++;
        }
        int z = 5;
        do{
            System.out.println("z :"+z);
        }while(z<4);
    }
}
