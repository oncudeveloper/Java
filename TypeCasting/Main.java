package TypeCasting;

public class Main {
    public static void main(String[] args) {
        int num = 5;
        double doubleNum = num;
        System.out.println(num);
        System.out.println(doubleNum);

        double number = 10.0;
        int intNum = (int) number;
        System.out.println(number);
        System.out.println(intNum);

        
        
    }
}
